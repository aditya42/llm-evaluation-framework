import time
from llm_eval_framework.providers.base import BaseLLMProvider, LLMResponse


class MockProvider(BaseLLMProvider):
    def generate(self, prompt: str) -> LLMResponse:
        start = time.perf_counter()
        prompt_lower = prompt.lower()

        if "ci/cd" in prompt_lower:
            text = "CI/CD means continuous integration and continuous delivery."
        elif "regression testing" in prompt_lower:
            text = "Regression testing validates existing functionality still works after changes."
        elif "hallucination" in prompt_lower:
            text = "An LLM hallucination is unsupported or incorrect information generated by a model."
        else:
            text = "Prioritize high-risk user journeys and automate stable regression scenarios."

        latency_ms = int((time.perf_counter() - start) * 1000)
        return LLMResponse(text=text, latency_ms=latency_ms, model="mock-provider")
